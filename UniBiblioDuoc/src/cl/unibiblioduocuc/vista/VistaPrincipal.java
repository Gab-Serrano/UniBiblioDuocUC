    private String getSelectedRadButton(ArrayList<JRadioButton> radioButtons) {
        for (JRadioButton radioButton : radioButtons) {
            if (radioButton.isSelected()) {
                return removeTildes(radioButton.getText());
            }
        }
        return "";
    }

    private String removeTildes(String input) {
        input = input.toLowerCase();  // Convertir a minúsculas
        input = input.replace('á', 'a');
        input = input.replace('é', 'e');
        input = input.replace('í', 'i');
        input = input.replace('ó', 'o');
        input = input.replace('ú', 'u');
        input = input.replace('ü', 'u');
        input = input.replace('ñ', 'n');
        // Agrega más reemplazos si es necesario
        return input;
    }

