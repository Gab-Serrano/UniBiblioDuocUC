    private void btnLblClicked(java.awt.event.MouseEvent evt) {
        JLabel clickedLabel = (JLabel) evt.getSource();

        switch (clickedLabel.getName()) {
            case "jBtn_Libros":
                resetAllButtonsAndPanels();
                setActive(jPanel_Libros, jPanelBtn_Libros, jBtn_Libros);
                break;
            case "jBtn_Prestamos":
                resetAllButtonsAndPanels();
                setActive(jPanel_Prestamos, jPanelBtn_Prestamos, jBtn_Prestamos);
                break;
            case "jBtn_Salir":
                int response = JOptionPane.showConfirmDialog(null, "¿Seguro que desea salir?", "Confirmación", 2, JOptionPane.YES_NO_OPTION);
                if (response == JOptionPane.YES_OPTION) {
                    dispose();
                    VistaLogin vl = new VistaLogin();
                    vl.setVisible(true);
                }
                break;
            default:
                throw new AssertionError();
        }
    }

    private void fillAllBooks() {
        ControladorPrincipal contLibros = new ControladorPrincipal();

        DefaultTableModel model = (DefaultTableModel) jTable_CatLibros.getModel();
        model.setRowCount(0);

        for (Libro libro : contLibros.obtenerLibros(bd)) {
            Object[] rowData = new Object[]{
                libro.getId(),
                libro.getTitulo(),
                libro.getNomAutor(),
                libro.getEditorial(),
                libro.getAnnoPublicacion(),
                libro.getGenero(),
                libro.getIsbn(),
                libro.getEstado(),
                libro.getColeccion()
            };
            model.addRow(rowData);
        }
    }

