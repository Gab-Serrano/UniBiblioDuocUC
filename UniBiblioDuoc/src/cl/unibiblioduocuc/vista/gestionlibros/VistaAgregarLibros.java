    private Libro setLibroObject() {
        ControladorPrincipal contLib = new ControladorPrincipal();
        Libro libroPorAgregar = new Libro();

        libroPorAgregar.setId(contLib.obtenerMaxId(bd));
        libroPorAgregar.setTitulo(jTxt_titulo.getText());
        libroPorAgregar.setNomAutor(jTxt_autor.getText());
        libroPorAgregar.setEditorial(jTxt_editorial.getText());
        int anno = (int) Integer.valueOf(jTxt_anno.getText());
        libroPorAgregar.setAnnoPublicacion(anno);
        libroPorAgregar.setGenero(jTxt_genero.getText());
        libroPorAgregar.setIsbn(jTxt_isbn.getText());

        libroPorAgregar.setColeccion((String) jCmbBox_coleccion.getSelectedItem());

        return libroPorAgregar;
    }
    
    private void setComboBox(){
        ControladorPrincipal contLib = new ControladorPrincipal();
        
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        for (String item : contLib.obtenerColecciones(this.bd)) {
            model.addElement(item);
        }
        
        jCmbBox_coleccion.setModel(model);
    }

